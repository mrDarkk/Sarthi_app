package com.bhupendra.farmers;

public class Executive
{

    public String e_name;
    public String e_imgUrl;
    public String e_email;
    public String e_phone;
    public String e_address;
    public String e_allocated_area;


    public Executive(String e_name, String e_imgUrl, String e_email, String e_phone, String e_address, String e_allocated_area) {
        this.e_name = e_name;
        this.e_imgUrl = e_imgUrl;
        this.e_email = e_email;
        this.e_phone = e_phone;
        this.e_address = e_address;
        this.e_allocated_area = e_allocated_area;
    }

}
