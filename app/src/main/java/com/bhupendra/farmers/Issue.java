package com.bhupendra.farmers;

public class Issue
{
    public String ex_mail;
    public String f_imgUrl;
    public String f_name;
    public String f_phone;
    public String f_type;
    public String f_issue;
    public String f_topic;
    public String f_no_of_member;
    public String f_activity;
    public String f_address;


    public Issue(String ex_mail, String f_imgUrl, String f_name, String f_phone, String f_type, String f_issue, String f_topic, String f_no_of_member, String f_activity, String f_address) {
        this.ex_mail = ex_mail;
        this.f_imgUrl = f_imgUrl;
        this.f_name = f_name;
        this.f_phone = f_phone;
        this.f_type = f_type;
        this.f_issue = f_issue;
        this.f_topic = f_topic;
        this.f_no_of_member = f_no_of_member;
        this.f_activity = f_activity;
        this.f_address = f_address;
    }
}
